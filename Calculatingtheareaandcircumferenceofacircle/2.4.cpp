#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    double area, circumference, pi, radius;
    pi =  3.1416;
    radius = 6;
    area = pi * pow(radius,2);
    circumference = 2 * pi * radius;
    
    cout << "The area is = " <<area;
    cout << "\nThe circumference is = " <<circumference;
    
    
    
    return 0;
}

